public class Aula_op_relacionais {
	public static void main(String[] args) {
		// ==(igual) != (diferente) < (menor) > (maior)
		
		int nota1 = 91;
		int nota2 = 90;
		
		if (nota1 != nota2) {
			System.out.println("as notas são iguais");
		} else {
			System.out.println("As notas são diferentes");
		}
	}
}
